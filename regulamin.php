§4.2 Każdy użytkownik może posiadać tylko jeden kanał.<br>
§4.3 Kanały w strefie prywatnej mogą zawierać maksymalnie 2 podkanały.<br>
§4.4 Brak numeru kanału lub zmiana go, jest sankcjowana jego usunięciem.<br>
§4.5 Kanał Prywatny musi posiadać nazwę oraz być zabezpieczony hasłem.<br>
§4.6 Administrator Kanału i Moderator Kanału są odpowiedzialni za pilnowanie porządku na kanale.<br>
§4.7 Rangę Administrator Kanału może posiadać tylko jedna osoba.<br>
§4.8 Rangę Channel Moderator Kanału mogą posiadać 2 osoby.<br>
